# 1. Write a Python program that Use while loop to display elements from a given list present at odd index positions

lst=[10,8,5,12,8,22,15]
index=0
odd_list=[]
while index<len(lst):
    odd_list.append(lst[index])
    index+=2
print(odd_list)    













